
$(window).scroll(function(){
    if ($(window).scrollTop() >= 126) {
        $('nav').addClass('fixed-header1');
        $('nav').addClass('fixed-header');
    }
  
    else {
        $('nav').removeClass('fixed-header1');
      $('nav').removeClass('fixed-header');
    }
});

